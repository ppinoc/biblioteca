package biblioteca;

/**
 *
 * @author peeet
 */
public class Socio {
    private int rut;
    private String direccion;
    private String nombre;

    public Socio() {
    }

    public Socio(int rut, String direccion, String nombre) {
        this.rut = rut;
        this.direccion = direccion;
        this.nombre = nombre;
    }

    public int getRut() {
        return rut;
    }

    public void setRut(int rut) {
        this.rut = rut;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
}

